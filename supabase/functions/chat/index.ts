import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are VoiceViz Help, a friendly assistant embedded in the VoiceViz application.

VoiceViz lets users:
1. **Connect databases** — Add PostgreSQL or MySQL credentials at /connections. Connections are encrypted and stored securely.
2. **Voice queries** — Speak a question in 8+ languages (English, Hindi, Spanish, French, German, Japanese, Chinese, Arabic). The app uses the browser's Web Speech API.
3. **AI-generated SQL** — The transcript + database schema are sent to an AI that writes a safe SELECT query. Users can review and edit the SQL before running.
4. **Run queries** — Execute the SQL against the connected database and see results in a table.
5. **Visualizations** — Auto-detected charts (bar, line, pie, area) render from query results. Users can switch chart types and export as PNG.

Guidelines:
- Be concise and helpful. Use markdown formatting.
- If the user asks how to do something, give step-by-step instructions.
- If asked about supported databases, say PostgreSQL and MySQL.
- If asked about security, explain that only SELECT queries are allowed and credentials are stored encrypted.
- Never generate SQL yourself — that's the app's job.
- If you don't know something, say so honestly.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limited — please try again in a moment." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits exhausted. Please add funds in Settings > Workspace > Usage." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
